package day07_Operators;

public class LogicalOperators {
    public static void main(String[] args) {

        //logical AND:
        boolean result1 = 10 >5 && 10 > 7;
                    //    true  &&   true

        System.out.println(result1);

        boolean result2 = 20 > 12 && 20 >25;
                         // ture && false
        System.out.println(result2);

    }
}
