package day04_Variables;

public class Currencies {
    public static void main(String[] args) {

        int dollar = 1000;

        double lira =dollar * 9.53;
        double euro =dollar * 0.86;

        //jpy,pound,peso,cad, riyal

        double jpy =dollar * 114.19;
        double pound =dollar * 0.73;
        double peso =dollar * 20.85;
        double cad =dollar * 1.24;
        double saudiRiyal =dollar * 3.751;
        double Afg =dollar * 90.90;

        System.out.println("lira = " + lira);
        System.out.println("euro = " + euro);
        System.out.println("jpy = " + jpy);
        System.out.println("pound = " + pound);
        System.out.println("peso = " + peso);
        System.out.println("cad = " + cad);
        System.out.println("saudiRiyal = " + saudiRiyal);
        System.out.println("Afg = " + Afg);




    }
}
