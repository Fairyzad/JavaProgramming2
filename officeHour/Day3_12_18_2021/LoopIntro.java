package Day3_12_18_2021;

public class LoopIntro {

    public static void main(String[] args) {
        for (int i = 0; i < 100; i++) {
            System.out.println("i =" +i);
        }
        System.out.println();

    }
}
/*
                 Classic for-loop counting up from 0 to 99
                 0, 1, 2, 3, ... 99
                -init int i = 0
                -test i<100
                -increment i++
                */